/**
 * Apply theme to CSS variables
 */
export function applyTheme(theme) {
  const root = document.documentElement;
  
  Object.entries(theme.colors).forEach(([key, value]) => {
    // Convert camelCase to kebab-case (e.g., hoverText -> hover-text)
    const kebabKey = key.replace(/([A-Z])/g, '-$1').toLowerCase();
    root.style.setProperty(`--color-${kebabKey}`, value);
  });
}

/**
 * Create a custom theme object
 */
export function createCustomTheme(name, colors) {
  return {
    name,
    colors: {
      primary: colors.primary || '#818cf8',
      secondary: colors.secondary || '#c084fc',
      background: colors.background || '#09090b',
      surface: colors.surface || '#18181b',
      text: colors.text || '#f8fafc',
      textSecondary: colors.textSecondary || '#a1a1aa',
      border: colors.border || '#27272a',
      hover: colors.hover || '#27272a',
      hoverText: colors.hoverText || colors.text || '#f8fafc',
    },
    isCustom: true
  };
}
